public class J03_연산처리 {
    public static void main(String[] args) {
        // 산출연산 출력하기
        System.out.println("산출연산 출력하기 ______________");
        System.out.println("3 + 5 = " + (3 + 5));
        System.out.println("3 - 5 = " + (3 - 5));
        System.out.println("3 / 5 = " + (3 / 5));
        System.out.println("3 * 5 = " + (3 * 5));
        System.out.println("3 % 5 = " + (3 % 5));
        System.out.println();



        // 실수연산 출력하기
        // 둘중에 하나라도 실수이면 결과도 실수 출력
        System.out.println("실수연산 출력하기 ______________");
        System.out.println("둘중에 하나라도 실수이면 결과도 실수 출력 ______________");
        System.out.println("5.0 / 2.0 = " + (5.0 / 2.0));
        System.out.println("5 / 2.0 = "+ (5 / 2.0));
        System.out.println("5.0 / 2 = "+ (5.0 / 2));
        System.out.println("19.0 / 2.0 =" + (19.0 / 2.0));
        System.out.println("5 * 2.0 =  "+ (5 * 2.0) );
        System.out.println("5.0 * 4.0 = " + (5.0 * 4.0));
    }
}
